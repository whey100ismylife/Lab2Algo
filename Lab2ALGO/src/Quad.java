public class Quad {
    int st;
    int nd;
    int rd;
    int th;

    public Quad(int st, int nd, int rd, int th){
        this.st = st;
        this.nd = nd;
        this.rd = rd;
        this. th = th;
    }

    // -------------  GETTERS ----------------


    public int getSt() {
        return st;
    }

    public int getNd() {
        return nd;
    }

    public int getRd() {
        return rd;
    }

    public int getTh() {
        return th;
    }
}
